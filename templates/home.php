<h1>Syarat Permohonan dan Pendaftaran Pelajar Perubatan Universiti di Mesir Sesi Akademik 2012/2013 </h1>
 <div id="lists">

  <ul>
  
  
<li>1. Pemohon yang ingin mengikuti pengajian bidang perubatan di universiti Mesir hendaklah memenuhi syarat kelayakan minimum yang telah ditetapkan oleh Kementerian Pengajian Tinggi Mesir iaitu :</li>

<li>Memperolehi Sijil Pelajaran Malaysia (SPM) dengan memperolehi 8 Kepujian dengan mendapat sekurang-kurangnya B4 (SPM sebelum tahun 2009) atau gred B (SPM tahun 2011) dalam; (Biologi, Fizik, Kimia, Matematik, Matematik Tambahan, Bahasa Melayu, Bahasa Inggeris dan Sejarah).</li>

<li>2. Pemohon juga hendaklah memenuhi semua syarat kelayakan yang telah ditetapkan oleh pihak Universiti di Mesir.</li>

<li>Ini adalah syarat minimum yang ditetapkan oleh KPT bagi memudahkan pelajar untuk mendapatkan Sijil Sokongan Luar Negara(SSLN).</li>

<li>Sijil Sokongan Luar negara (SSLN) merupakan satu syarat yang dikenakan kepada pelajar yang ingin mengikuti kursus-kursus di luar negara bagi menentukan mereka memenuhi syarat kelayakan minimum untuk mengikuti program pengajian ke luar negara. Ia juga digunakan untuk memperoleh visa pelajar daripada Kedutaan negara luar untuk melanjutkan pelajaran ke institusi pendidikan di negara berkaitan. SSLN diperkenalkan sejak tahun 2002.</li>

  </ul>
