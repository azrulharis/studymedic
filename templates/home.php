<h1>Pengenalan Pengajian Program Mansoura-Manchester</h1>

<p>Universiti Mansoura menawarkan dua program bagi bidang perubatan iaitu: Conventional Program dan Manchester Program. Boleh dikatakan hampir semua universiti di Mesir menggunakan sistem Conventional kecuali Alexandria University.
</p><p>
Untuk makluman semua, Manchester Program dan Conventional Program ini berbeza dari sudut silibus, cara pembelajaran serta cara belajar dan juga berlainan sistem peperiksaan. Walau berbeza cara, matlamatnya tetap jua sama – dah graduate jadi doktor juga. Insya-Allah.
</p><p>
Dalam Manchester Program, terdapat dua sem untuk setahun. Setiap sem, para pelajar akan belajar satu topik, contohnya pelajar sem tiga, akan belajar topik cardio-respiratory fitness. Maka, sepanjang sem tiga tersebut akan belajar segala yang berkaitan dengan cardio-respiratory fitness - segenap sudut. Para pelajar diajar dengan menggunakan Program Based Learning (PBL).
</p>
<h2>Apa itu PBL?</h2>
<p>
PBL merupakan nama singkatan bagi Program Based Learning. Dalam program ni, pelajar akan diberikan satu kes setiap minggu, selalunya setiap sem ada 10 kes, maksudnya, total 20 kes untuk setahun. PBL, dalam bahasa mudah dan ringkas merupakan kumpulan kecil. Dalam setiap PBL, ada lebih kurang 11 orang, dan setiap PBL mempunyai mentor mereka sendiri yang terdiri daripada doktor universiti. PBL ni tempat untuk berbincang dengan ahli PBL masing-masing setelah menghadiri kuliah umum.
</p>
<h2>Aktiviti sepanjang PBL</h2>
<p>
Kes yang diberikan selalunya dalam bentuk karangan. Dalam bentuk karangan itulah terdapat segala maklumat mengenai pesakit, patients complain dan sebgainya. Ahli kumpulan PBL seterusnya, memilih seorang chairman untuk kendalikan kes PBL tersebut.
</p><p>
Selalunya, PBL dimulakan dengan bacaan kes dari setiap ahli. Kemudian ahli kumpulan akan cari difficult words and try to define it, contohnya "dysphagia", "abscess", etc. Kemudian, chairman akan meminta ahli kumpulan mencadangkan objektif. Objektif tersebut merupakan assignment mingguan pelajar - e.g, physiology of pain etc. Objektif tersebut bukanlah setakat satu mahupun dua, boleh jadi berbelas-belas.
</p><p>
PBL berikutnya, pelajar diminta untuk menjelaskan segala yang telah dipelajari hasil kajian (study) objektif bagi case tersebut pada ahli PBL lain, doktor cuma akan tambah sikit-sikit – sekadar mentor.
</p>
<h2>Jadual harian</h2>
<p>
Pelajar perlu menghadiri kuliah umum selama lebih kurang dua jam dan kelas praktikal selama sejam. Apabila dah sem tiga, pelajar didedahkan dengan pendedahan awal di hospital (chest department and radiological imaging) tapi bila sem enam baru ada early experience clinical (melibatkan pesakit).
</p>
<h2>Dari mana nak dapat bahan belajar?</h2>
<p>
Tidak ada buku yang wajib dibeli khas untuk program ini. Untuk buku, boleh saja beli buku department (macam conventional) takpun pinjam buku senior kemudian photostat ataupun boleh beli buku rujukan antarabangsa seperti: Netter Atlas and Guyton & Hall Physiology. Lecture notes, slides doktor dalam lecture merupakan eleman paling penting sebab slide itu lah guideline untuk belajar.
</p>
<h2>Exam, macam mana pula?</h2>
<p>
Jumlah kertas yang perlu diduduki oleh pelajar adalah sebanyak tiga kertas. Kertas pertama ialah MCQ, yang mana korang cuma perlu menghitamkan borang OMR tu. Kedua berbentuk kertas esei dan yang terakhir sekali OSCE exam. OSCE merupakan bentuk peperiksaan yang melibatkan practical lessons dan juga histology slide. Pelajar dikehendaki menjawab atas kertas asing berdasarkan soalan pada komputer.</p>



